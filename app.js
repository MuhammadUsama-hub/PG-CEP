function calc1()
{
    var interest=document.getElementById('interest').value;
    var capital=document.getElementById('capital').value;
    var operational=document.getElementById('operational').value;
    var unitsPerAnnum=1707800*(0.7957/100)*8760;
    var totalCapCost=capital*unitsPerAnnum;
    var totalOperCost=operational*unitsPerAnnum;
    var fixedCost=(interest/100)*totalCapCost;
    var totalAnulCost=fixedCost+totalOperCost;
    var costPerUnit=totalAnulCost/unitsPerAnnum;
    document.getElementById('Opportunity Cos').innerHTML=totalOperCost.toFixed(3);
    document.getElementById('Capital Cost').innerHTML=totalCapCost.toFixed(3);
    document.getElementById('Annual Cost').innerHTML=totalAnulCost.toFixed(3);
    document.getElementById('Unit').innerHTML=costPerUnit;
  
}
